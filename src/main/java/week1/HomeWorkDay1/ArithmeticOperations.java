package week1.HomeWorkDay1;

class ArithmeticOperations 
{
	static int numberOne=22;
	static int numberTwo=44;

	public void addition() 
	{
		System.out.println("Sum of Two Number : ");
		System.out.println(numberOne  + numberTwo);
	}  
	
	public void multiply() 
	{
		System.out.println("Product of Two Number : ");
		System.out.println(numberOne  * numberTwo);
	}  
	
	public void subtract() 
	{
		System.out.println("Diference of Two Number : ");
		System.out.println(numberTwo  - numberOne);
	}  
	
	public void multiplyNoByTwo() 
	{
		System.out.println("First number multiplied by 2 :");
		System.out.println(numberOne  * 2);
		System.out.println("Second number multiplied by 2 :");
		System.out.println(numberTwo  * 2);
	}  
	
	public void divideNoByTwo() 
	{
		System.out.println("First number divided by 2 :");
		System.out.println(numberOne  / 2);
		System.out.println("First number divided by 2 :");
		System.out.println(numberTwo  / 2);
	} 
}
